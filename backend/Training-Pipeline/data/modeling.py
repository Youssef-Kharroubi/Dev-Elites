    "def detect_skew_angle(image, max_angle_threshold=5.0):\n",
    "    \"\"\"Detect the skew angle of an image, returning 0 if below threshold.\"\"\"\n",
    "    if len(image.shape) == 3:  # Convert color to grayscale\n",
    "        gray = cv2.cvtColor(image, cv2.COLOR_BGR2GRAY)\n",
    "    else:\n",
    "        gray = image\n",
    "        \n",
    "    # Threshold and find contours\n",
    "    _, thresh = cv2.threshold(gray, 0, 255, cv2.THRESH_BINARY_INV + cv2.THRESH_OTSU)\n",
    "    contours, _ = cv2.findContours(thresh, cv2.RETR_LIST, cv2.CHAIN_APPROX_SIMPLE)\n",
    "    \n",
    "    # Find minimum area rectangle\n",
    "    if not contours:\n",
    "        return 0\n",
    "    largest_contour = max(contours, key=cv2.contourArea)\n",
    "    rect = cv2.minAreaRect(largest_contour)\n",
    "    angle = rect[-1]\n",
    "    \n",
    "    # Improved angle normalization\n",
    "    if angle < -45:\n",
    "        angle = 90 + angle  # Convert (-90° to -45°) to (0° to 45°)\n",
    "    else:\n",
    "        angle = -angle  # Flip sign to match rotation direction\n",
    "    \n",
    "    # Only return angle if it exceeds threshold\n",
    "    return angle if abs(angle) > max_angle_threshold else 0"
